class Solution{
public:
    vector<int> optimalArray(int n,vector<int> &a){
        // Code here
        int mid;
        int pre_moves=0;
        vector<int>moves;
        for(int i=0;i<n;i++){
            mid=i/2;
            pre_moves+=a[i]-a[mid];
            moves.push_back(pre_moves);
        }
        return moves;
    }
};

